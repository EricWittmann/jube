package org.jboss.jube.model;

/**
 * Represents a local host node
 */
public class HostNode {
    private String id;
    private String hostName;

    @Override
    public String toString() {
        return "HostNode{" +
                "id='" + id + '\'' +
                ", hostName='" + hostName + '\'' +
                '}';
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        HostNode hostNode = (HostNode) o;

        if (hostName != null ? !hostName.equals(hostNode.hostName) : hostNode.hostName != null) return false;
        if (id != null ? !id.equals(hostNode.id) : hostNode.id != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id != null ? id.hashCode() : 0;
        result = 31 * result + (hostName != null ? hostName.hashCode() : 0);
        return result;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getHostName() {
        return hostName;
    }

    public void setHostName(String hostName) {
        this.hostName = hostName;
    }
}
